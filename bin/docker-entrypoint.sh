generate_secret() {
  python3 - <<'PY'
import secrets

print(secrets.token_urlsafe(32))
PY
}

config_value() {
  local key="$1"
  [[ -f "$CONFIG_FILE" ]] || return 1
  awk -v key="$key" '
    index($0, key "=") == 1 {
      value = substr($0, length(key) + 2)
      gsub(/^"|"$/, "", value)
      print value
      exit
    }
  ' "$CONFIG_FILE"
}

set_config_value() {
  local key="$1"
  local value="$2"
  local tmp_file=""

  tmp_file="$(mktemp "$CONFIG_FILE.XXXXXX")"
  awk -v key="$key" -v value="$value" '
    BEGIN { replaced = 0 }
    index($0, key "=") == 1 {
      print key "=" value
      replaced = 1
      next
    }
    { print }
    END {
      if (!replaced) {
        print key "=" value
      }
    }
  ' "$CONFIG_FILE" >"$tmp_file"
  chmod 600 "$tmp_file"
  mv "$tmp_file" "$CONFIG_FILE"
}

repair_placeholder_secret() {
  local key="$1"
  local initialized_marker="$2"
  local value=""

  value="$(config_value "$key" 2>/dev/null || true)"
  if [[ -n "$value" && "$value" != "change-me" ]]; then
    return 0
  fi
  if [[ -z "$value" && -e "$initialized_marker" ]]; then
    set_config_value "$key" "change-me"
    return 0
  fi
  if [[ -e "$initialized_marker" ]]; then
    return 0
  fi

  set_config_value "$key" "$(generate_secret)"
}

